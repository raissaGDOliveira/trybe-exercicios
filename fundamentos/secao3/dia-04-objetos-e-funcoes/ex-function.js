//Faça cinco programas, um para cada operação aritmética básica.
//Seu programa deve ter duas constantes, a e b, definidas no começo com os valores que serão operados.
//Faça programas para:
//Adição (a + b)
function sum(a, b) {
    return a + b;
}
console.log(sum(2, 5));
//Subtração (a - b)
function sub(a, b) {
    return a - b;
}
console.log(sub(2, 5));
//Multiplicação (a * b)
function multip(a, b) {
    return a * b;
}
console.log(multip(2, 5));
//Divisão (a / b)
function div(a, b) {
    return a / b;
}
console.log(div(2, 5));
//Módulo (a % b)
function modl(a, b) {
    return a % b;
}
console.log(modl(5, 5));